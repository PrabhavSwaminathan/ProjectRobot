package com.company;

public class Coordinates {
    int x;
    int y;

    public Coordinates(){
        x=0;
        y=0;
    }
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
    public void setX(int x) {
        this.x = x;
    }
    public void setY(int y) {
        this.y = y;
    }
}
